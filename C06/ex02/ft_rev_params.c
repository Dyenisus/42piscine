#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (av[j])
		j++;
	if (ac != 0)
	{
		while (j - 1 != 0)
		{
			while (av[j - 1][i])
			{
				write(1, &av[j - 1][i], 1);
				i++;
			}
			write(1, "\n", 1);
			i = 0;
			j--;
		}
	}
	return (0);
}
