#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	if (ac != 0)
	{
		while (av[j])
		{
			while (av[j][i])
			{
				write(1, &av[j][i], 1);
				i++;
			}
			write(1, "\n", 1);
			i = 0;
			j++;
		}
	}
	return (0);
}
